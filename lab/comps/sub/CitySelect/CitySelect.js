export default class CitySelect extends LAB.Component {
  static propTypes = {
    itemStyleClass: PropTypes.string,
  };
  static defaultProps = {
    itemStyleClass: 'default',
  };

  constructor(props, context) {
    super(props, context);
    this.state = {
      show: false,
      index: this.props.select && this.props.select.length
        ? this.props.select.length
        : 0,
      select: this.props.select || [],
      data: props.data || getCityData(),
    };
    this.defaultStyles = styles;
  }

  componentWillReceiveProps(newProps) {
    if (newProps.select && newProps.select != this.state.select) {
      this.setState({
        select: newProps.select,
        index: newProps.select.length,
      });
    }
    if (this.props.data !== newProps.data) {
      this.state.data = newProps.data || getCityData();
    }
  }

  setValue(newValue) {
    this.setState({
      select: newValue,
    });
  }

  getValue() {
    return this.state.select;
  }

  render() {
    let items = [];
    let temp = [];
    for (let i in this.state.data['1']) {
      temp.push(
        <Select.Item key={i} label={this.state.data['1'][i]} value={i} />
      );
    }
    items.push(temp);
    temp = [];
    for (let i in this.state.data[this.state.select[0]]) {
      temp.push(
        <Select.Item
          key={i}
          label={this.state.data[this.state.select[0]][i]}
          value={i}
        />
      );
    }
    items.push(temp);
    temp = [];
    for (let i in this.state.data[this.state.select[1]]) {
      temp.push(
        <Select.Item
          key={i}
          label={this.state.data[this.state.select[1]][i]}
          value={i}
        />
      );
    }
    items.push(temp);
    return (
      <View style={this.getStyle('container')}>
        <Select
          ref="select"
          style_class={this.props.itemStyleClass}
          style={{ flex: 1 }}
          show={this.state.show}
          defaultText={
            this.state.select[0] && this.state.select[0] != -1 ? null : '省.市.区'
          }
          label="请选择省"
          text={this.state.data['1'][this.state.select[0]]}
          onChange={i => {
            this.onChange(0, i);
          }}
          onCancel={() => {
            this.setState({ show: false });
          }}
        >
          {items[0]}
        </Select>
        {this.state.index > 0
          ? <Select
              ref="select"
              style_class={this.props.itemStyleClass}
              style={{ flex: 1 }}
              show={this.state.show}
              defaultText={
                this.state.select[1] && this.state.select[1] != -1
                  ? null
                  : '市.区'
              }
              label="请选择市"
              text={
                this.state.data[this.state.select[0]] &&
                this.state.data[this.state.select[0]][this.state.select[1]]
              }
              onChange={i => {
                this.onChange(1, i);
              }}
              onCancel={() => {
                this.setState({ show: false });
              }}
            >
              {items[1]}
            </Select>
          : <View style={this.getStyle('blockView')} />}
        {this.state.index > 1
          ? <Select
              ref="select"
              style_class={this.props.itemStyleClass}
              style={{ flex: 1 }}
              show={this.state.show}
              defaultText={
                this.state.select[2] && this.state.select[2] != -1 ? null : '区'
              }
              label="请选择区"
              text={
                this.state.data[this.state.select[1]] &&
                this.state.data[this.state.select[1]][this.state.select[2]]
              }
              onChange={i => {
                this.onChange(2, i);
              }}
              onCancel={() => {
                this.setState({ show: false });
              }}
            >
              {items[2]}
            </Select>
          : <View style={this.getStyle('blockView2')} />}
      </View>
    );
  }

  onChange(index, i) {
    let temp = [];
    for (let j = 0; j < index; j++) {
      temp.push(this.state.select[j]);
    }
    temp[index] = i;
    this.setState({
      index: i > 0 ? index + 1 : index, //当前处于第几个索引 用于隐藏其他项
      select: temp,
    });
    this.props.onChange && this.props.onChange(temp);
    index == 2 && this.props.onChangeEnd && this.props.onChangeEnd(temp);
  }
}

const styles = StyleSheet.create({
  container: {
    flexDirection: 'row',
  },
  blockView: {
    flex: 1,
  },
  blockView2: {
    flex: 1,
  },
});
